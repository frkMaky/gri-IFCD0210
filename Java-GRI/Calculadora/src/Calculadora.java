
public class Calculadora {
	
	public static void main(String [] args) {
		
        /*Realiza un proyecto nuevo
        * con una clase llamada Calculadora que declare dos variables de
        * valores 12 y 6 y queremos
        * mostrar la suma, la resta, el producto y el cociente
        */
		
		double num1 = 12;
		double num2 = 6;
		
		System.out.println("La suma de 12 y 6 es " + (num1 + num2));
		System.out.println("La resta de 12 y 6 es " + (num1 - num2));
		System.out.println("El producto de 12 y 6 es " + (num1 * num2));
		System.out.println("El cociente de 12 y 6 es " + (num1 / num2));
		
		
	}
}
